public class loop {
    public static void main(String[] args) {
        // for loop
        for (int i = 0; i < 5; i++) {
            System.out.println("The value of i is: " + i);
        }
        // while loop
        int j = 0;
        while (j < 5) {
            System.out.println("The value of j is: " + j);
            j++;
        }
        // do-while loop
        int k = 0;
        do {
            System.out.println("The value of k is: " + k);
            k++;
        } while (k < 5);
    }
}
